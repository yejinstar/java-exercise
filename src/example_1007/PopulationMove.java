package example_1007;

public class PopulationMove {
    private int fromSido;
    private int toSido;

    //constructor
    public PopulationMove(int fromSido, int toSido) {
        this.fromSido = fromSido;
        this.toSido = toSido;
    }

    public int getToSido() {
        return toSido;
    }
    public int getFromSido() {
        return fromSido;
    }


}
